interface AnimatedImageRef {
  show: ({
    x,
    y,
    newX,
    newY,
    zIndex,
  }: {
    x: number
    y: number
    zIndex: number
    newX: number
    newY: number
  }) => void
  isActive: () => boolean
}

const AnimatedImage = forwardRef<AnimatedImageRef, { src: string }>(
  ({ src }, ref) => {
    const controls = useAnimation()
    const isRunning = useRef(false)
    const imgRef = useRef<HTMLImageElement>(null)

    useImperativeHandle(ref, () => ({
      isActive: () => isRunning.current,
      show: async ({
        x,
        y,
        newX,
        newY,
        zIndex,
      }: {
        x: number
        y: number
        zIndex: number
        newX: number
        newY: number
      }) => {
        const rect = imgRef.current?.getBoundingClientRect()
        if (!rect) {
          return
        }

        const center = (posX: number, posY: number) => {
          const coords = {
            x: posX - rect.width / 2,
            y: posY - rect.height / 2,
          }
          return `translate(${coords.x}px, ${coords.y}px)`
        }

        controls.stop()

        controls.set({
          opacity: isRunning.current ? 1 : 0.75,
          zIndex,
          transform: `${center(x, y)} scale(1)`,
          transition: { ease: "circOut" },
        })

        isRunning.current = true

        await controls.start({
          opacity: 1,
          transform: `${center(newX, newY)} scale(1)`,
          transition: { duration: 0.9, ease: "circOut" },
        })

        await Promise.all([
          controls.start({
            transition: { duration: 1, ease: "easeInOut" },
            transform: `${center(newX, newY)} scale(0.1)`,
          }),
          controls.start({
            opacity: 0,
            transition: { duration: 1.1, ease: "easeOut" },
          }),
        ])

        isRunning.current = false
      },
    }))

    return (
      <motion.img
        ref={imgRef}
        initial={{ opacity: 0, scale: 1 }}
        animate={controls}
        src={src}
        alt="trail element"
        className="absolute h-56 w-44 object-cover"
      />
    )
  }
)

AnimatedImage.displayName = "AnimatedImage"

